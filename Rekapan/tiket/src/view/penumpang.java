    public void GetTable() {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Identitas");
        model.addColumn("No.identitas");
        model.addColumn("Kode KAI");
        model.addColumn("Nama");
        model.addColumn("TTL");
        model.addColumn("Alamat");
        
        //menampilkan data database kedalam tabel
        try {
            int no = 1;
            String sql = "SELECT * FROM penumpang";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            
            while (rs.next()) {
                model.addRow(new Object[]{no++, 
                    rs.getString("identitas"), 
                    rs.getString("no_identitas"),
                    rs.getString("kode_kai"),
                    rs.getString("nama_penumpang"), 
                    rs.getString("tgl_lahir"),
                    rs.getString("alamat") 
                    
                });
            }
            tabel.setModel(model);
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }

