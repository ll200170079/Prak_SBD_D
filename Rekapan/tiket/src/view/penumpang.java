    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        try {
            int line = tabel.getSelectedRow();
            inidentitas.setSelectedItem(tabel.getValueAt(line, 1).toString());
            inno_identitas.setText(tabel.getValueAt(line, 2).toString());
            kode.setText(tabel.getValueAt(line, 3).toString());
            nama.setText(tabel.getValueAt(line, 4).toString());
            DefaultTableModel Model =(DefaultTableModel) tabel.getModel();
            java.util.Date Tgl = new SimpleDateFormat("d MMM yyyy").parse((String) Model.getValueAt(line, 5)) ;
            ttl.setDate(Tgl);
            inalamat.setText(tabel.getValueAt(line, 6).toString());
            // TODO add your handling code here:
        }catch(ParseException ex){
                Logger.getLogger(penumpang.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_tabelMouseClicked
    
